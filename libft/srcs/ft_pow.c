#include "libft.h"

t_ull				ft_pow(t_ull x, int p)
{
	t_ull	res;

	res = 1;
	if (p < 0)
		p = 0;
	while (p)
	{
		if (p & 1)
		{
			res *= x;
			--p;
		}
		x *= x;
		p >>= 1;
	}
	return (res);
}
